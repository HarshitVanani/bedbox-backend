// backend/models/MaintenanceNotice.js
const mongoose = require('mongoose');

const MaintenanceNoticeSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
    trim: true
  },
  description: {
    type: String,
    required: true,
    trim: true
  },
  areaOrLocation: {
    type: String,
    required: true,
    trim: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model('MaintenanceNotice', MaintenanceNoticeSchema);